<li><u>事工</u></li>
<br>1. 宣教培訓:　配合牧師的培訓計劃，鼓勵執事，團契組長，及對宣教有心的會員，都參加訓練課程，培養宣教意識。
<br>&nbsp;<br>

<br>2. 確認對象: 對不同的禾場，進行聯絡，研究其需要及機會，并本教會可做的事工， 作適當的配合。
<br>&nbsp;<br>

<br>3. 本地佈道: 配合團契關懷部，準備福音營，佈道會各項事工。
<br>&nbsp;<br>

<br>4. 聯 絡關懷: 聯絡本教會支持的宣教士，牧師，定期予以關懷，以瞭解其需要及難處，設法給予適當的幫助。
<br>&nbsp;<br>

<br>5. 建立團隊: 差傳委員會按需要，不定期召開會議。成員將以邀請方式產生，無任期限制。
<br>&nbsp;<br>

<br>6. 禱告支援: 爲本教會支持的宣教士，牧師，以禱告托住他們的事工和家庭。
<br>&nbsp;<br>
